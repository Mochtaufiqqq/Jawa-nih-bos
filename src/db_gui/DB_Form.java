    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            Connection conn = connjav.getConnection();
            ClientPreparedStatement kratos = (ClientPreparedStatement) conn.prepareStatement("insert into tb_crud(nama, kelas, umur)values(?, ?, ?)");
            kratos.setString(1, jTextField1.getText());
            kratos.setString(2, jComboBox1.getSelectedItem().toString());
            kratos.setString(3, jTextField2.getText());
            kratos.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan","pesan", JOptionPane.INFORMATION_MESSAGE);
            TampilData();
        }catch (SQLException e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

