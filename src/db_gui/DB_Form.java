public void TampilData(){
     tb_crud = new DefaultTableModel();
     tb_crud.addColumn("id");
     tb_crud.addColumn("nama");
     tb_crud.addColumn("kelas");
     tb_crud.addColumn("umur");
     jTable1.setModel(tb_crud);
     Connection conn = connjav.getConnection();
     try{
         java.sql.Statement stmt = conn.createStatement();
         SQL = "SELECT * FROM tb_crud";
         java.sql.ResultSet Zeus = stmt.executeQuery(SQL);
         while (Zeus.next()){
         tb_crud.addRow(new Object[]{
             Zeus.getString("id"),
             Zeus.getString("nama"),
             Zeus.getString("kelas"),
             Zeus.getString("umur")
         });
         }
     }catch (SQLException e){
         System.out.println("e.getMessage()");
     }
}

