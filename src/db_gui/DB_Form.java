    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Connection conn = connjav.getConnection();
        int confirm = JOptionPane.showConfirmDialog(null,
                "Apakah anda yakin ingin menghapus data tsb?",
               "konfirmasi", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (confirm == 0){
            try{
                java.sql.PreparedStatement Garp = conn.prepareStatement("delete from tb_crud where id ='"
                        + jTextField3.getText()+"'");
                Garp.executeUpdate();
                JOptionPane.
                        showMessageDialog(null, "Data berhasii di hapus","pesan"
                        ,JOptionPane.INFORMATION_MESSAGE);
                TampilData();
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField1.requestFocus();
            }catch (SQLException e){
                JOptionPane.showMessageDialog(null,"Data gagal di hapus" + e.getMessage(), "pesan",JOptionPane.ERROR_MESSAGE );

            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

